package gokv

import "github.com/tochemey/gokv/internal/internalpb"

// Entry represents the key/value pair
type Entry struct {
	// Key represents the key
	Key string
	// Value represents the value
	Value []byte
}

func fromNode(entry *internalpb.Entry) *Entry {
	return &Entry{
		Key:   entry.Key,
		Value: entry.Value,
	}
}
